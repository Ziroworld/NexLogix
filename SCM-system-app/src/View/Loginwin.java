    private void openUpdateFrame() {
        frame = new ForgotPasswordView();
        frame.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                frame.dispose(); // Only close the update frame
            }
        });
        frame.setVisible(true);
    }
    
